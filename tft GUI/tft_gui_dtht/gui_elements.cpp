ImageFromSRAM::ImageFromSRAM(uint16_t* dt, uint16_t ww, uint16_t hh)
{
    arr = dt;
    w = ww, h = hh;
}
void draw(int x, int y)
{
    uint32_t i = 0;
    for (int row = h - 1; row >= 0; row--)
    {
        for (int c = 0; c < w; c++)
        {
            tft.drawPixel(x + c, y + row, pgm_read_word_near(arr + i));
            i++;
        }
    }
}


